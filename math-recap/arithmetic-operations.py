# flood division // will divide but not show any decimal places
print(f"3//5: {3//5} \n3/5: {3/5}")
print(f"9//5: {9//5} \n9/5: {9/5}")

# Square root can be expressed as exponential calcuations
print(8 ** (1/3))  # cube root
print(8 ** (1/2))  # square root

# Function int() will convert numbers to integers
print(int(3.8))

# Function float() will turn numbers into floats
print(float(47))

# *Note you can not turn a string integer into a float
# using float(), nor can you turn a string float into an integer using int() 

# You can use .is_integer() to check what it is
print(1.8.is_integer())
